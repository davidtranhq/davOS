void terminal_init()
{}

void terminal_write(const char *str, size_t size)
{
    for (size_t i = 0; i < size; ++i)
    {
        if (str[i] == '\n')
        {
            cursor_newline();
        }
        else
        {
            uint8_t padding = 1;
            uint64_t x = cursor_x * (font_width + padding);
            uint64_t y = cursor_y * (font_height + padding);
            draw_char(str[i], x, y);
            cursor_advance();
        }
    }
}
uint16_t font_width = 8;
uint16_t font_height = 16;

volatile struct limine_framebuffer_request request =
{
    .id = LIMINE_FRAMEBUFFER_REQUEST,
    .revision = 1
};

limine_framebuffer *terminal_framebuffer = request.response->framebuffers[0];
uint64_t width = terminal_framebuffer->width;
uint64_t height = terminal_framebuffer->height;
uint64_t cursor_x = 0;
uint64_t cursor_y = 0;
uint64_t max_line_length = 100;

/**
 * @brief Move the cursor to the start of a new line.
 */
void cursor_newline()
{
    cursor_x = 0;
    // TODO: implement terminal scrolling
    cursor_y += 1;
}

/**
 * @brief Advance the cursor one character.
 */
void cursor_advance()
{
    cursor_x += 1;
    if (cursor_x > max_line_length)
        cursor_newline();
}

/**
 * @brief Draw a white pixel at (x, y).
 */
void draw_pixel(uint64_t x, uint64_t y)
{
    uint32_t *buffer = static_cast<uint32_t *>(terminal_framebuffer->address);

    const uint32_t white_pixel = 0xffffffff;
    buffer[y * width + x] = white_pixel;
}

/**
 * @brief Draw an 8x12 character at (x,y).
 */
void draw_char(char ch, uint64_t x, uint64_t y)
{
    uint16_t glyph_index = static_cast<uint16_t>(ch) * font_height;
    uint8_t *glyph = font_bytes + glyph_index;

    for (uint8_t glyph_row = 0; glyph_row < font_height; ++glyph_row)
    {
        for (uint8_t glyph_col = 0; glyph_col < font_width; ++glyph_col)
        {
            bool pixel_set = glyph[glyph_row] & (1 << (font_width - glyph_col - 1));
            if (pixel_set)
                draw_pixel(x + glyph_col, y + glyph_row);
        }
    }
}

